// getSiteWithID gets a *Site from SitesDB with matching input site ID.
// When no entry exists, a new entry is made and returned.
func getSiteWithID(siteid string) *Site {
	site, ok := SitesDB[siteid]
	if ok {
		return site
	}
	nSite := &Site{
		ID: siteid,
	}
	SitesDB[siteid] = nSite
	return nSite
}

